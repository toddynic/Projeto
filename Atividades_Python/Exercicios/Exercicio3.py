#Cálculo de Área de um Retângulo

#Pede a largura e a altura e calcula a área do retangulo com esses tamanhos

largura = float(input("Fale a largura do retangulo em cm: "))
altura = float(input("\nFale a altura do retangulo em cm: "))

print(f"\n\nArea do retangulo: {(largura*altura):.2f} cm")